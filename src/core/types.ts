/* ------------------------------------------------------------------ queries */

export interface DateRange {
  from: Timestamp | null;
  to: Timestamp | null;
}

/** The full query surface. Every field is optional and ANDed together. */
export interface SearchQuery {
  /** Raw user text, including any `key:value` filter syntax. */
  text: string;
  tags: string[];
  technologies: string[];
  languages: string[];
  projects: string[];
  statuses: SessionStatus[];
  range: DateRange;
  /** When false, archived sessions are excluded. */
  includeArchived: boolean;
  sort: SearchSort;
  limit: number;
  offset: number;
}

export const SEARCH_SORTS = ['relevance', 'recent', 'oldest', 'complexity', 'title'] as const;
export type SearchSort = (typeof SEARCH_SORTS)[number];

export interface SearchHit {
  session: Session;
  /** Higher is better. Combined BM25 + recency + importance, see `rank.ts`. */
  score: number;
  /** `[start, end)` offsets into the field, for highlighting. */
  highlights: Highlight[];
  /** Which matching strategy produced the hit — surfaced in the UI as a chip. */
  via: 'exact' | 'fts' | 'fuzzy' | 'filter';
}

export interface Highlight {
  field: 'title' | 'project' | 'summary' | 'notes' | 'body' | 'tags' | 'technologies';
  start: number;
  end: number;
}

export interface SearchResult {
  hits: SearchHit[];
  /** Total matches ignoring limit/offset. */
  total: number;
  /** Wall-clock milliseconds, shown in the search HUD. */
  elapsedMs: number;
}

/** Aggregate counts used by the filter sidebar and the stats panel. */
export interface Facets {
  languages: FacetBucket[];
  projects: FacetBucket[];
  tags: FacetBucket[];
  technologies: FacetBucket[];
  statuses: FacetBucket[];
}

export interface FacetBucket {
  value: string;
  count: number;
}

export interface LibraryStats {
  sessionCount: number;
  messageCount: number;
  artifactCount: number;
  fileCount: number;
  projectCount: number;
  tokenEstimate: number;
  earliest: Timestamp | null;
  latest: Timestamp | null;
  /** Bytes on disk for the SQLite database. */
  databaseBytes: number;
}

/* ----------------------------------------------------------------- defaults */

export const EMPTY_QUERY: SearchQuery = {
  text: '',
  tags: [],
  technologies: [],
  languages: [],
  projects: [],
  statuses: [],
  range: { from: null, to: null },
  includeArchived: false,
  sort: 'relevance',
  limit: 200,
  offset: 0,
};

export function emptyQuery(overrides: Partial<SearchQuery> = {}): SearchQuery {
  return {
    ...EMPTY_QUERY,
    ...overrides,
    tags: [...(overrides.tags ?? [])],
    technologies: [...(overrides.technologies ?? [])],
    languages: [...(overrides.languages ?? [])],
    projects: [...(overrides.projects ?? [])],
    statuses: [...(overrides.statuses ?? [])],
    range: { ...EMPTY_QUERY.range, ...overrides.range },
  };
}

/** True when the query would match everything (used to skip search entirely). */
export function isBlankQuery(q: SearchQuery): boolean {
  return (
    q.text.trim() === '' &&
    q.tags.length === 0 &&
    q.technologies.length === 0 &&
    q.languages.length === 0 &&
    q.projects.length === 0 &&
    q.statuses.length === 0 &&
    q.range.from === null &&
    q.range.to === null
  );
}
